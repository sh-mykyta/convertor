package org.example;

public class ConvertOperations {

    public static double sekToUsd(double num){
        return num * 0.1;

    }

    public static double usdToSek(double num){
        return num * 9.67;
    }

    public static double sekToEuro(double num){
        return num * 0.09;
    }

    public static double euroToSek(double num){
        return num * 11.08;
    }

    public static double euroToUsd(double num){
        return num * 1.15;
    }

    public static double usdToEuro(double num){
        return num * 0.87;
    }

}
